// Develop a program to find the sum of numbers from 1 to 100 using a loop.

public class Sum1To100 {

    public static void main(String[] args) {

        int num = 0;

        // for loop
        for (int i = 1; i < 101; i++) {
            num += i;
        }

        // print value
        System.out.println(num);
    }
}
